package pc.materials.bean;

public class KeyboardDataDTO {
	private int k_num;
	private String k_bossKey;
	private int k_code;
	private String k_company;
	private String k_name;
	private String k_type;
	private String k_connector;
	
	
	public int getK_num() {
		return k_num;
	}
	public void setK_num(int k_num) {
		this.k_num = k_num;
	}
	public String getK_bossKey() {
		return k_bossKey;
	}
	public void setK_bossKey(String k_bossKey) {
		this.k_bossKey = k_bossKey;
	}
	public int getK_code() {
		return k_code;
	}
	public void setK_code(int k_code) {
		this.k_code = k_code;
	}
	public String getK_company() {
		return k_company;
	}
	public void setK_company(String k_company) {
		this.k_company = k_company;
	}
	public String getK_name() {
		return k_name;
	}
	public void setK_name(String k_name) {
		this.k_name = k_name;
	}
	public String getK_type() {
		return k_type;
	}
	public void setK_type(String k_type) {
		this.k_type = k_type;
	}
	public String getK_connector() {
		return k_connector;
	}
	public void setK_connector(String k_connector) {
		this.k_connector = k_connector;
	}
	
	
}

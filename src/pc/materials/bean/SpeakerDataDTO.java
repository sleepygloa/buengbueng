package pc.materials.bean;

public class SpeakerDataDTO {
	private int s_num;
	private String s_bossKey;
	private int s_code;
	private String s_company;
	private String s_name;
	private String s_connector;
	
	
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public String getS_bossKey() {
		return s_bossKey;
	}
	public void setS_bossKey(String s_bossKey) {
		this.s_bossKey = s_bossKey;
	}
	public int getS_code() {
		return s_code;
	}
	public void setS_code(int s_code) {
		this.s_code = s_code;
	}
	public String getS_company() {
		return s_company;
	}
	public void setS_company(String s_company) {
		this.s_company = s_company;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_connector() {
		return s_connector;
	}
	public void setS_connector(String s_connector) {
		this.s_connector = s_connector;
	}
	
}

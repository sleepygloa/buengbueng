package fx.user.bean;

public class FxUserInfoDTO {
	
	private String totalId;
	private String totalName;
	private String totalBirth;
	private String totalPhone;
	private String totalAddress;
	private String totalEmail;
	private String totalGoogleId;
	public String getTotalId() {
		return totalId;
	}
	public void setTotalId(String totalId) {
		this.totalId = totalId;
	}
	public String getTotalName() {
		return totalName;
	}
	public void setTotalName(String totalName) {
		this.totalName = totalName;
	}
	public String getTotalBirth() {
		return totalBirth;
	}
	public void setTotalBirth(String totalBirth) {
		this.totalBirth = totalBirth;
	}
	public String getTotalPhone() {
		return totalPhone;
	}
	public void setTotalPhone(String totalPhone) {
		this.totalPhone = totalPhone;
	}
	public String getTotalAddress() {
		return totalAddress;
	}
	public void setTotalAddress(String totalAddress) {
		this.totalAddress = totalAddress;
	}
	public String getTotalEmail() {
		return totalEmail;
	}
	public void setTotalEmail(String totalEmail) {
		this.totalEmail = totalEmail;
	}
	public String getTotalGoogleId() {
		return totalGoogleId;
	}
	public void setTotalGoogleId(String totalGoogleId) {
		this.totalGoogleId = totalGoogleId;
	}
	
	
	
}
